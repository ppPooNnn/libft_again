#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	res;
	size_t	i;

	i = 0;
	while ((s1[i] || s2[i]) && i < n)
	{
		if (s1[i] != s2[i])
		{
			res = (unsigned char)s1[i] - (unsigned char)s2[i];
			return (res);
		}
		i++;
	}
	return (0);
}
